#include "calc.h"

/**
 * op_add - add two numbers
 * @a: first num
 * @b: second num
 * Return: sum of nums
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate diff of two numbers
 * @a: first num
 * @b: second num
 * Return: diff of two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: first num
 * @b: second num
 * Return: mul of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first num
 * @b: second num
 * Return: div of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod of num
 * @a: first num
 * @b: second num
 * Return: mod of num
 */

int op_mod(int a, int b)
{
	return (a % b);
}
